package modeloBeans;


public class BeansCarro {
    
    private String carplaca;
    private String cardono;
    private String carmodelo;
    private String carstatus;
    private String carbusca;
    
    public String getCarbusca() {
        return carbusca;
    }

    public void setCarbusca(String carbusca) {
        this.carbusca = carbusca;
    }

    public String getCarplaca() {
        return carplaca;
    }

    public void setCarplaca(String carplaca) {
        this.carplaca = carplaca;
    }


    public String getCardono() {
        return cardono;
    }

    public void setCardono(String cardono) {
        this.cardono = cardono;
    }

    public String getCarmodelo() {
        return carmodelo;
    }

    public void setCarmodelo(String carmodelo) {
        this.carmodelo = carmodelo;
    }

    public String getCarstatus() {
        return carstatus;
    }

    public void setCarstatus(String carstatus) {
        this.carstatus = carstatus;
    }
    
}
